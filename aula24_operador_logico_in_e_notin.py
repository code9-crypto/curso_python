#OPERADOR IN E NOT IN
#in -> está entre
#not in -> não está entre
#IMPORTANTE: Uma string é iterável e por isso é possível verificar(desta forma)
#se uma letra ou conjunto de letras encontra-se naquela palavra ou até mesmo texto
nome = "William"
print("USANDO O IN")
print("a" in nome)
print("m" in nome)
print(10 * "-")
print("USANDO O NOT IN")
print("Wi" not in nome)
print("had" not in nome)