#ADIÇÃO +
print( 1 + 1)

#SUBSTRAÇÃO -
print( 1 - 1 )

#MULTIPLICAÇÃO *
print( 1 * 1 )

#DIVISÃO /
#OBS.: o resultado será retornará como ponto flutuante(float)
print( 10 / 3 )

#DIVISÃO INTEIRA
#OBS.: esta divisão não retorna todas as casas decimais(como a de cima).
#O seu retorno ou será inteiro(sem casa decimal) ou será com .0
print(10 // 7.5)

#EXPONENCIAÇÃO / POTENCIAÇÃO
print( 2 ** 10 )

#MÓDULO (resto da divisão)
print( 10 % 3 )